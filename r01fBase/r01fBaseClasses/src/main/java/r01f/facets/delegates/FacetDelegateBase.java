package r01f.facets.delegates;

import lombok.experimental.Accessors;

/**
 * Base type for all content model object's facet delegates t
 * @param <T>
 */
@Accessors(prefix="_")
public abstract class FacetDelegateBase<T> { 
/////////////////////////////////////////////////////////////////////////////////////////
//  FIELDS
/////////////////////////////////////////////////////////////////////////////////////////
	/**
	 * Model object
	 */
	protected final T _modelObject;
/////////////////////////////////////////////////////////////////////////////////////////
//  CONSTRUCTOR
/////////////////////////////////////////////////////////////////////////////////////////
	public FacetDelegateBase(final T modelObject) {
		_modelObject = modelObject;
	}
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	protected T getModelObject() {
		return _modelObject;
	}
}
