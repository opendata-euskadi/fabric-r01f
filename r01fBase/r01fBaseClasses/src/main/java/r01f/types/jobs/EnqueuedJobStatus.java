package r01f.types.jobs;

import r01f.enums.EnumExtended;
import r01f.enums.EnumExtendedWrapper;

/**
 * The status of an enqueued job
 */
public enum EnqueuedJobStatus 
 implements EnumExtended<EnqueuedJobStatus> {
	NOT_STARTED,				
	STARTED,
	PAUSED,
	FINALIZED_ERROR,
	FINALIZED_OK;
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	private static EnumExtendedWrapper<EnqueuedJobStatus> WRAPPER = EnumExtendedWrapper.wrapEnumExtended(EnqueuedJobStatus.class);
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	public static EnqueuedJobStatus fromName(final String name) {
		return WRAPPER.fromName(name);
	}
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	@Override
	public boolean isIn(final EnqueuedJobStatus... els) {
		return WRAPPER.isIn(this,els);
	}
	@Override
	public boolean is(final EnqueuedJobStatus el) {
		return WRAPPER.is(this,el);
	}
	
}
